#include "3-calc.h"

/**
 * op_add - operation addition
 * @a: first integer
 * @b: second integer
 * -------------------------
 * Return: operation
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - operation substrain
 * @a: first integer
 * @b: second integer
 * -------------------------
 * Return: operation
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - operation multiply
 * @a: first integer
 * @b: second integer
 * -------------------------
 * Return: operation
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - operation division
 * @a: first integer
 * @b: second integer
 * -------------------------
 * Return: operation
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - operation module
 * @a: first integer
 * @b: second integer
 * -------------------------
 * Return: operation
 */
int op_mod(int a, int b)
{
	return (a % b);
}
